void esp_event(char *recvBuf) {
	int i = 0;
	char *pToken;
	char *pArray[ARR_CNT] = { 0 };
	char sendBuf[MAX_UART_COMMAND_LEN] = { 0 };

	int target;

	strBuff[strlen(recvBuf) - 1] = '\0';	//'\n' cut
	printf("Debug recv : %s\r\n", recvBuf);

	pToken = strtok(recvBuf, "[@]");
	while (pToken != NULL) {
		pArray[i] = pToken;
		if (++i >= ARR_CNT)
			break;
		pToken = strtok(NULL, "[@]");
	}

	if (!strcmp(pArray[1], "BOILER")) {
		if (!strcmp(pArray[2], "ON")) {
			strcpy(sender, pArray[0]);
			boilerFlag = 1;
			MX_GPIO_LED_ON(LD1_Pin | LD3_Pin | LD2_Pin);
			sprintf(sendBuf, "[%s]%s@%s\n", pArray[0], pArray[1], pArray[2]);
		}
		else if (!strcmp(pArray[2], "OFF")) {
			strcpy(sender, "ALL_MSG");
			boilerFlag = 0;
			MX_GPIO_LED_OFF(LD1_Pin | LD3_Pin | LD2_Pin);
			sprintf(sendBuf, "[%s]%s@%s\n", pArray[0], pArray[1], pArray[2]);
		}
		else if (!strcmp(pArray[2], "TARGET")) {
			target = atoi(pArray[3]);
			sprintf(sendBuf, "[%s]%s@%s@%d\n", pArray[0], pArray[1], pArray[2],
					target);
		}
	}
	else if (!strcmp(pArray[1], "GAS")) {
		if (atoi(pArray[2]) > 1000) {
			boilerFlag = 1;
			MX_GPIO_LED_ON(LD1_Pin | LD3_Pin | LD2_Pin);
			sprintf(sendBuf, "[%s]BOILER@ON\n", pArray[0]);
		}
	}

	else if (!strncmp(pArray[1], " New conn", 8)) {
//	   printf("Debug : %s, %s\r\n",pArray[0],pArray[1]);
		return;
	} else if (!strncmp(pArray[1], " Already log", 8)) {
// 	    printf("Debug : %s, %s\r\n",pArray[0],pArray[1]);
		esp_client_conn();
		return;
	} else
		return;

	esp_send_data(sendBuf);

	printf("Debug send : %s\r\n", sendBuf);
}
